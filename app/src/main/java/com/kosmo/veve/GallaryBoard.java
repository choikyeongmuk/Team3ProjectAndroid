package com.kosmo.veve;


public class GallaryBoard {
    private String gallary_no;
    private String title;
    private String content;
    private String postDate;
    private String userID;
    private String visitCount;
    private String scrapCount;
    private String commentCount;
    private String gallary_file_no;
    private String f_path;
    private String f_name;
    private String fileOne;



    public String getGallary_no() {
        return gallary_no;
    }

    public void setGallary_no(String gallary_no) {
        this.gallary_no = gallary_no;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getPostDate() {
        return postDate;
    }

    public void setPostDate(String postDate) {
        this.postDate = postDate;
    }

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }

    public String getVisitCount() {
        return visitCount;
    }

    public void setVisitCount(String visitCount) {
        this.visitCount = visitCount;
    }

    public String getScrapCount() {
        return scrapCount;
    }

    public void setScrapCount(String scrapCount) {
        this.scrapCount = scrapCount;
    }

    public String getCommentCount() {
        return commentCount;
    }

    public void setCommentCount(String commentCount) {
        this.commentCount = commentCount;
    }


    public String getGallary_file_no() {
        return gallary_file_no;
    }

    public void setGallary_file_no(String gallary_file_no) {
        this.gallary_file_no = gallary_file_no;
    }

    public String getF_path() {
        return f_path;
    }

    public void setF_path(String f_path) {
        this.f_path = f_path;
    }

    public String getF_name() {
        return f_name;
    }

    public void setF_name(String f_name) {
        this.f_name = f_name;
    }

    public String getFileOne() {
        return fileOne;
    }

    public void setFileOne(String fileOne) {
        this.fileOne = fileOne;
    }
}